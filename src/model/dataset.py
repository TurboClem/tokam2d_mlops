"""Dataset wrappers used during training."""

import torch
from torch.utils.data import Dataset, Subset


class TransformSubset(Subset):
    """Subset that applies a transform and filters degenerate boxes."""

    def __init__(self, ds, idx, tf):
        super().__init__(ds, idx)
        self.transform = tf

    def __getitem__(self, i):
        img, tgt = self.dataset[self.indices[i]]
        if self.transform:
            img, tgt = self.transform(img, tgt)
        if tgt["boxes"] is not None and len(tgt["boxes"]):
            b = tgt["boxes"]
            valid = (b[:, 2] > b[:, 0]) & (b[:, 3] > b[:, 1])
            tgt["boxes"] = b[valid] if valid.any() else None
            tgt["labels"] = tgt["labels"][valid] if valid.any() else None
        return img, tgt


class PseudoLabelDataset(Dataset):
    """Holds pseudo-labeled turb_i frames (generated by the model during training)."""

    def __init__(self, frames, targets, transform=None):
        self.frames = frames
        self.targets = targets
        self.transform = transform

    def __len__(self):
        return len(self.frames)

    def __getitem__(self, i):
        img = self.frames[i].clone()
        tgt = {
            k: v.clone() if isinstance(v, torch.Tensor) else v
            for k, v in self.targets[i].items()
        }
        if self.transform:
            img, tgt = self.transform(img, tgt)
        if tgt["boxes"] is not None and len(tgt["boxes"]):
            b = tgt["boxes"]
            valid = (b[:, 2] > b[:, 0]) & (b[:, 3] > b[:, 1])
            tgt["boxes"] = b[valid] if valid.any() else None
            tgt["labels"] = tgt["labels"][valid] if valid.any() else None
        return img, tgt
